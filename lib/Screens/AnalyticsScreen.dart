  Widget _buildSalesChart() {
    return Container(
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(20),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withOpacity(0.05),
            blurRadius: 10,
            offset: const Offset(0, 4),
          ),
        ],
      ),
      child: Padding(
        padding: const EdgeInsets.all(20),
        child: SizedBox(
          height: 300,
          child: StreamBuilder<QuerySnapshot>(
            stream: _getOrdersQuery().snapshots(),
            builder: (context, snapshot) {
              if (snapshot.connectionState == ConnectionState.waiting) {
                return const Center(child: CircularProgressIndicator());
              }

              if (!snapshot.hasData || snapshot.data!.docs.isEmpty) {
                return _buildEmptyState(
                  icon: Icons.bar_chart_outlined,
                  message: 'No sales data available for this range.',
                );
              }

              // Aggregate sales by day
              final ordersByDay =
                  snapshot.data!.docs.fold<Map<DateTime, double>>(
                {},
                (map, doc) {
                  final data = doc.data() as Map<String, dynamic>;
                  final timestamp = data['timestamp'] as Timestamp;
                  final date = timestamp.toDate();
                  final day = DateTime(date.year, date.month, date.day);
                  final total = (data['totalAmount'] as num?)?.toDouble() ?? 0;
                  map[day] = (map[day] ?? 0) + total;
                  return map;
                },
              );

              // Generate all days in the range
              final List<DateTime> allDays = [];
              DateTime current = DateTime(_dateRange.start.year,
                  _dateRange.start.month, _dateRange.start.day);
              final end = DateTime(_dateRange.end.year, _dateRange.end.month,
                      _dateRange.end.day)
                  .add(const Duration(days: 1));

              while (current.isBefore(end)) {
                allDays.add(current);
                current = current.add(const Duration(days: 1));
              }

              // Prepare chart data
              final chartData = allDays.map((day) {
                return SalesData(
                  day,
                  ordersByDay[day] ?? 0,
                  DateFormat('MMM dd').format(day),
                );
              }).toList();

              return SfCartesianChart(
                primaryXAxis: CategoryAxis(
                  majorGridLines: const MajorGridLines(width: 0),
                  axisLine: const AxisLine(width: 0),
                  labelStyle: TextStyle(color: Colors.black87, fontSize: 12),
                ),
                primaryYAxis: NumericAxis(
                  title: AxisTitle(
                    text: 'Sales Amount (QAR)',
                    textStyle: TextStyle(
                        color: Colors.black87, fontWeight: FontWeight.bold),
                  ),
                  majorGridLines:
                      const MajorGridLines(width: 0.5, color: Colors.grey),
                  axisLine: const AxisLine(width: 0),
                  labelStyle: TextStyle(color: Colors.black87, fontSize: 12),
                ),
                tooltipBehavior: TooltipBehavior(
                  enable: true,
                  header: '',
                  canShowMarker: false,
                  animationDuration: 0,
                  color: Colors.deepPurpleAccent,
                  textStyle: const TextStyle(color: Colors.white, fontSize: 14),
                  format: 'QAR point.y',
                ),
                series: <CartesianSeries<SalesData, String>>[
                  ColumnSeries<SalesData, String>(
                    dataSource: chartData,
                    xValueMapper: (SalesData sales, _) => sales.label,
                    yValueMapper: (SalesData sales, _) => sales.amount,
                    gradient: LinearGradient(
                      colors: [
                        Colors.deepPurple.shade300,
                        Colors.deepPurple.shade600,
                      ],
                      begin: Alignment.topCenter,
                      end: Alignment.bottomCenter,
                    ),
                    borderRadius: BorderRadius.circular(8),
                    width: 0.7,
                    animationDuration: 1000,
                  ),
                ],
              );
            },
          ),
        ),
      ),
    );
  }

  Widget _buildTopItemsList() {
    return Container(
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(20),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withOpacity(0.05),
            blurRadius: 10,
            offset: const Offset(0, 4),
          ),
        ],
      ),
      child: Padding(
        padding: const EdgeInsets.all(20),
        child: StreamBuilder<QuerySnapshot>(
          stream: _getOrdersQuery().snapshots(),
          builder: (context, snapshot) {
            if (snapshot.connectionState == ConnectionState.waiting) {
              return const Center(child: CircularProgressIndicator());
            }

            if (!snapshot.hasData || snapshot.data!.docs.isEmpty) {
              return _buildEmptyState(
                icon: Icons.restaurant_menu_outlined,
                message: 'No items sold in selected range.',
              );
            }

            final itemCounts = <String, int>{};
            final itemRevenue = <String, double>{};

            for (var doc in snapshot.data!.docs) {
              final data = doc.data() as Map<String, dynamic>;
              final items =
                  List<Map<String, dynamic>>.from(data['items'] ?? []);

              for (var item in items) {
                final itemName = item['name'] ?? 'Unknown Item';
                final quantity = (item['quantity'] as num?)?.toInt() ?? 1;
                final price = (item['price'] as num?)?.toDouble() ?? 0;

                itemCounts.update(itemName, (value) => value + quantity,
                    ifAbsent: () => quantity);
                itemRevenue.update(
                  itemName,
                  (value) => value + (price * quantity),
                  ifAbsent: () => price * quantity,
                );
              }
            }

            if (itemCounts.isEmpty) {
              return _buildEmptyState(
                icon: Icons.restaurant_menu_outlined,
                message: 'No items sold in selected range.',
              );
            }

            final sortedItems = itemCounts.entries.toList()
              ..sort((a, b) => b.value.compareTo(a.value));
            final topItems = sortedItems.take(5).toList();

            return ListView.builder(
              shrinkWrap: true,
              physics: const NeverScrollableScrollPhysics(),
              itemCount: topItems.length,
              itemBuilder: (context, index) {
                final item = topItems[index];
                return Container(
                  margin: const EdgeInsets.only(bottom: 12),
                  padding: const EdgeInsets.all(16),
                  decoration: BoxDecoration(
                    color: Colors.grey[50],
                    borderRadius: BorderRadius.circular(16),
                    border: Border.all(
                      color: Colors.deepPurple.withOpacity(0.1),
                    ),
                  ),
                  child: Row(
                    children: [
                      Container(
                        width: 50,
                        height: 50,
                        decoration: BoxDecoration(
                          shape: BoxShape.circle,
                          gradient: LinearGradient(
                            colors: [
                              Colors.deepPurple.shade300,
                              Colors.deepPurple.shade500,
                            ],
                          ),
                        ),
                        child: Center(
                          child: Text(
                            '#${index + 1}',
                            style: const TextStyle(
                              fontWeight: FontWeight.bold,
                              fontSize: 16,
                              color: Colors.white,
                            ),
                          ),
                        ),
                      ),
                      const SizedBox(width: 16),
                      Expanded(
                        child: Column(
                          crossAxisAlignment: CrossAxisAlignment.start,
                          children: [
                            Text(
                              item.key,
                              style: const TextStyle(
                                fontWeight: FontWeight.bold,
                                fontSize: 16,
                                color: Colors.black87,
                              ),
                            ),
                            const SizedBox(height: 4),
                            Text(
                              'Revenue: QAR ${itemRevenue[item.key]?.toStringAsFixed(2) ?? '0.00'}',
                              style: TextStyle(
                                color: Colors.grey[600],
                                fontSize: 14,
                              ),
                            ),
                          ],
                        ),
                      ),
                      Column(
                        crossAxisAlignment: CrossAxisAlignment.end,
                        children: [
                          Text(
                            '${item.value}',
                            style: const TextStyle(
                              fontWeight: FontWeight.bold,
                              fontSize: 18,
                              color: Colors.green,
                            ),
                          ),
                          Text(
                            'sold',
                            style: TextStyle(
                              fontSize: 12,
                              color: Colors.grey[600],
                            ),
                          ),
                        ],
                      ),
                    ],
                  ),
                );
              },
            );
          },
        ),
      ),
    );
  }

